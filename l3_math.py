print('сторона вписанного квадрата:',v) #выводим на экран сторону вписанного квадрата
n= int(a) * (3**0.5) #вычисление стороны вписанного равностороннего треугольника
print('сторона вписанного равностороннего треугольника:',n) #выводим на экран сторону вписанного равностороннего треугольника
z= 2 * int(a) #вычисление стороны описанного квадрата
print('сторона описанного квадрата:',z) #выводим на экран сторону описанного квадрата
q= int(a) * 2 * (3**0.5) #вычисление стороны описанного треугольника
print('сторона описанного треугольника:',q) #вывод на экран стороны описанного треугольника
